<h1>Funktionen</h1>
<p>Nachfolgend eine Übersicht über die Funktionen von Rogers Assistant:</p>
<br />
<h3>Shortcuts</h3>
<p>Dank der Funktion Shortcuts können jegliche Applikationen oder Datein mithilfe Tastenkombinationen geöffnet werden. Dazu ein Beispiel:<br/> Herr Muster braucht öfters den Webbrowser <a href="http://www.mozilla-europe.org">Mozilla Firefox</a> und muss diesen immer im Startmenü suchen, damit er im Internet surfen kann. Statt ihn nun jedesmal im Startmenü mit mehreren Mausklicks zu öffnen, kann er in Rogers Assistant die Tastenkombination "Windowstaste + F" definieren. Drückt er nun die Windowstaste mit der F-Taste, öffnet sich automatisch der Firefox.</p>
<br />
<h3>Easy Ordner</h3>
<p>Mit der Funktion Easy Ordner kann ein Kontextmenü mit einstellbaren Ordner per Druck auf das Mausrad geöffnet werden. Drückt man nun in einem Öffnen, Speichern, usw.-Dialog, im Explorer oder in der Konsole das Mausrad, erscheint das Menü und man kann einen Ordner auswählen, der dann automatisch geöffnet wird.
<img src="images/beispiel_easy_ordner.png" alt=" Beispiel eines Easy Ordner Kontextmenüs " title=" Beispiel eines Easy Ordner Kontextmenüs " />
</p>
<br />
<h3>Google</h3>
<p>Mithilfe dieser Funktion lässt sich ein markiertes Wort oder markierten Bereich mit der Tastenkombination "Windowstaste + G" bei der Suchmaschine <a href="http://www.google.ch">Google</a> nachschlagen. Falls kein Wort markiert wurde, erscheint ein Eingabefenster, in das der gewünschte Suchtext eingegeben werden kann.</p>
<img src="images/beispiel_eingabefenster.png" alt=" Beispiel Eingabefenster " title=" Beispiel Eingabefenster " />
<br />
<br />
<h3>Wikipedia</h3>
<p>Diese Funktion ähnelt der Funktion Google, jedoch wird bei dieser das markierte Wort oder die Eingabe im Eingabefenster nicht bei Google nachgeschlagen, sondern bei der <a href="http://de.wikipedia.org">freien Enzyklopädie Wikipedia</a>.</p>
<br />
<h3>LEO</h3>
<p>Auch diese Funktion ähnelt den Funktionen Google und Wikipedia, nur das bei dieser das markierte Wort oder die Eingabe im Eingabefenster beim <a href="http://dict.leo.org/">Onlineüberstetzungsdienst LEO (Deutsch-Englisch) der Universität München</a>  in die jeweils andere Sprache übersetzt wird.</p>
